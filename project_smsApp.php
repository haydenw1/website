      <p>This article uses JavaScript and D3.js (a JavaScript visualization library) to create the animations that occur when a user interacts with the graph. If the user chooses an industry that is related to another app story, the article will display a link to that story at the bottom of the page (shown at the end of the .gif below).</p>

