      <h1>Approach</h1>
      <h2 id="spareOrphan">Adobe Experience Manager&mdash;Mobile</h2>
      <p>The Media Arts and Technology program at the Rochester Institute of Technology has connections with Adobe's digital publishing team; because of this connection, we were offered an opportunity to develop an application to serve as a student study using Adobe's new digital publishing beta: Adobe Experience Manager&mdash;Mobile.</p>
      <p>This software moves away from monthly digital publication releases towards
        fluid, constantly updating content with a higher level of user interaction.</p>

      <h2>Interactive Articles</h2>
      <p>As the "technical lead" of this project I was responsible for creating
        and testing interactive content, and handling app settings, layout, and distribution.</p>

        <p>Interactive articles for the app were created using HTML, CSS, and JavaScript
        to ensure proper device resizing and a high level of user interaction. Adobe's
        software allowed me to directly publish these html articles to our application.</p>

